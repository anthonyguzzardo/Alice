/**
 * 30 seed questions for Alice.
 *
 * Principles:
 * 1. Unanswerable in one sentence — creates friction.
 * 2. About you, not a topic — not a seminar question.
 * 3. No right answer — you're thinking, not performing.
 * 4. Worth returning to — your answer today differs from three months from now.
 */
export const SEED_QUESTIONS: string[] = [
  // Week 1: Surface — what's already in motion
  "What are you pretending isn't bothering you right now?",
  "If you couldn't work on anything you're currently working on, what would you do instead?",
  "What decision are you quietly avoiding?",
  "What would you be embarrassed to admit you want?",
  "When was the last time you changed your mind about something that mattered?",
  "What do you keep almost saying out loud but don't?",
  "What's the difference between what you say you value and how you actually spend your time?",

  // Week 2: Dig — the patterns underneath
  "What are you building, and who told you to build it?",
  "What would you do if you knew nobody was watching?",
  "What question are you afraid someone will ask you?",
  "When do you feel most like yourself? What's different about those moments?",
  "What have you outgrown but haven't let go of yet?",
  "What's the story you tell yourself about why things haven't worked out the way you wanted?",
  "What would it look like to take yourself seriously?",

  // Week 3: Turn — the harder stuff
  "What are you protecting by staying busy?",
  "If your life had a thesis statement, what would it be? Do you like it?",
  "What would you have to give up to get what you actually want?",
  "Who do you become when you're scared? Is that who you want to be?",
  "What conversation would change everything if you actually had it?",
  "What are you building that will still matter in ten years?",
  "Where are you performing competence instead of actually learning?",

  // Week 4: Settle — direction and identity
  "What would you work on if you had no audience?",
  "What's the most honest thing you could say about where you are right now?",
  "What do you know that you wish you didn't?",
  "What's the version of your life you're most afraid of ending up in?",
  "What are you circling that you haven't landed on yet?",
  "If you could only do one thing for the next year, what would it be?",
  "What would it mean to stop optimizing and start choosing?",

  // Days 29-30: Return
  "Go back and read your first response. What do you notice?",
  "What question should Alice have asked you that it didn't?",
];
